namespace GameEngine.Entities
{
    /// <summary>
    /// The class contains properties to pass them as parameters.
    /// It was created to make shorter input parameters.
    /// </summary>
    public class PointsCoordinates
    {
        /// <summary>
        /// First X point.
        /// </summary>
        public int FirstXPoint { get; set; }

        /// <summary>
        /// Second X point.
        /// </summary>
        public int SecondXPoint { get; set; }

        /// <summary>
        /// First Y point.
        /// </summary>
        public int FirstYPoint { get; set; }

        /// <summary>
        /// Second Y point.
        /// </summary>
        public int SecondYPoint { get; set; }
    }
}
